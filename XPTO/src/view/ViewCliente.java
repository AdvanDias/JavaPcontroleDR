    private void View_Cli_Bt_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_View_Cli_Bt_ExcluirActionPerformed
        // TODO add your handling code here:
        int linha = View_Cli_Tabela.getSelectedRow();
        if(linha < 0){
            JOptionPane.showMessageDialog(this, "Cliente não selecionado", "Alerta", JOptionPane.INFORMATION_MESSAGE);
        }else{
            int codigo = (int) View_Cli_Tabela.getValueAt(linha, 0);
            if(controllerCliente.excluirClienteController(codigo)){
                carregarListaCliente();
                limparFormulario();
                JOptionPane.showMessageDialog(this, "Usuário Excluído", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            }else {
                JOptionPane.showMessageDialog(this, "Erro ao Excluir", "Erro", JOptionPane.ERROR);   
            }
            
            
        }
    }//GEN-LAST:event_View_Cli_Bt_ExcluirActionPerformed

