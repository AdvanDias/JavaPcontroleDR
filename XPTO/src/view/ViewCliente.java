    private void View_Cli_Bt_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_View_Cli_Bt_SalvarActionPerformed
        // TODO add your handling code here:
        modelCliente = new ModelCliente();
        modelCliente.setId(Integer.parseInt(View_Cli_Id.getText()));
        modelCliente.setNome(View_Cli_Nome.getText());
        modelCliente.setCpf(View_Cli_CPF.getText().replace(" ", "").replace(".", "").replace("-", ""));
        modelCliente.setCnpj(View_Cli_CNPJ.getText().replace(" ", "").replace(".", "").replace("-", "").replace("/", ""));
        modelCliente.setSexo(View_ComboBox_M_F.getSelectedItem().toString());
        modelCliente.setInscricao_estadual(View_Cli_InscEstadual.getText());
        modelCliente.setRazao_social(View_Cli_RSocial.getText());
        modelCliente.setNome_fantasia(View_Cli_NFantasia.getText());
        modelCliente.setTelefone(View_Cli_Tel.getText().replace(" ", "").replace("(", "").replace(") ", "").replace("-", ""));
        modelCliente.setEmail(View_Cli_Email.getText());
        modelCliente.setclasse_cli(View_ComboBox_PJ_PF.getSelectedItem().toString());
        modelCliente.setCapital(Float.parseFloat(View_Con_Capital.getText().replace("R$", "").replace(".", "").replace(",", ".")));
      
        
        if(AltCadas.equals("Cadastrar")){
            
            if(View_ComboBox_PJ_PF.getSelectedItem().toString() == "-"){
                 JOptionPane.showMessageDialog(this, "Erro os Campos Classe deve ser Preenchidos", "Erro", JOptionPane.ERROR_MESSAGE);
            }else{
                 if(controllerCliente.salvarUsuarioController(modelCliente)){
                //cadastrou
                    JOptionPane.showMessageDialog(this, "Cadastro Realizado com Sucesso!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                    //limpar
                    limparFormulario();
                    carregarListaCliente();
                    carregaCodigo();
                    if(!View_Cli_Id.getText().isEmpty()){
                        
                        viewEndereco.enviaidcliente(modelCliente.getId());
                        viewEndereco.setLocationRelativeTo(null);
                        viewEndereco.setVisible(true);
                        viewEndereco.setResizable(false);
                        setVisible(false);
                    }
                    
                }else{
                //não cadastrou
                    JOptionPane.showMessageDialog(this, "Erro ao Cadastrar", "Erro", JOptionPane.ERROR_MESSAGE);
                }


            }
        }else{
            if(View_ComboBox_PJ_PF.getSelectedItem().toString() == "-"){
             JOptionPane.showMessageDialog(this, "Erro o Campo Classe deve ser Preenchidos", "Erro", JOptionPane.ERROR_MESSAGE);
            }else{
                 if(controllerCliente.atualizarUsuarioController(modelCliente)){
                  //Alterar
                        JOptionPane.showMessageDialog(this, "Cliente Atualizado com Sucesso!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                        //limpar
                        limparFormulario();
                        carregarListaCliente();
                        carregaCodigo();
                    }else{
                    //não cadastrou
                        JOptionPane.showMessageDialog(this, "Erro ao Atualizar", "Erro", JOptionPane.ERROR_MESSAGE);
                    }


                }
        }

    }//GEN-LAST:event_View_Cli_Bt_SalvarActionPerformed

