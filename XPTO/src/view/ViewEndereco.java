    private void View_End_Bt_AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_View_End_Bt_AlterarActionPerformed
        // TODO add your handling code here:
           AltCadas = "Alterar";
           modelEndereco = new ModelEndereco();
        int linha = View_End_Tabela.getSelectedRow();
        if(linha < 0){
            JOptionPane.showMessageDialog(this, "Endereço não selecionado", "Alerta", JOptionPane.INFORMATION_MESSAGE);
        }else{
            int idEndere = (int) View_End_Tabela.getValueAt(linha, 0);
            modelEndereco = controllerEndereco.getEnderecoController(idEndere);
            
            View_Ender_Endereco.setText(modelEndereco.getEndereco());
            View_Ender_Numero.setText(modelEndereco.getNumero());
            View_Ender_Bairro.setText(modelEndereco.getBairro());
            View_Ender_Comple.setText(modelEndereco.getComplemento());
            View_Ender_Cidade.setText(modelEndereco.getCidade());
            View_Ender_UF.setText(modelEndereco.getUf());
            View_Ender_CEP.setText(modelEndereco.getCep());
            View_ende_cli_id.setText(Integer.toString(modelEndereco.getCliente_id_cliente()));
            
            
            

        }
    }//GEN-LAST:event_View_End_Bt_AlterarActionPerformed

    private void View_End_Bt_ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_View_End_Bt_ExcluirActionPerformed
        // TODO add your handling code here:
        int linha = View_End_Tabela.getSelectedRow();
        if(linha < 0){
            JOptionPane.showMessageDialog(this, "Endereço não selecionado", "Alerta", JOptionPane.INFORMATION_MESSAGE);
        }else{
            int codigo = (int) View_End_Tabela.getValueAt(linha, 0);
            if(controllerEndereco.excluirEnderecoController(codigo)){
                carregarListaEndereco();
                
                JOptionPane.showMessageDialog(this, "Endereço Excluído", "Atenção", JOptionPane.INFORMATION_MESSAGE);
            }else {
                JOptionPane.showMessageDialog(this, "Erro ao Excluir", "Erro", JOptionPane.ERROR);   
            }
            
            
        }
    }//GEN-LAST:event_View_End_Bt_ExcluirActionPerformed

