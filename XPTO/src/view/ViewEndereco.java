    private void carregarListaEndereco(){
        listaEndereco = controllerEndereco.getListaEnderecoController();
        DefaultTableModel modelo = (DefaultTableModel) View_End_Tabela.getModel();
        modelo.setNumRows(0);
        
        for(int i = 0; i < listaEndereco.size(); i++){
            
                modelo.addRow(new Object[]{
                listaEndereco.get(i).getId_ende(),
                listaEndereco.get(i).getEndereco(),
                listaEndereco.get(i).getNumero(),
                listaEndereco.get(i).getComplemento(),
                listaEndereco.get(i).getBairro(),
                listaEndereco.get(i).getCidade(),
                listaEndereco.get(i).getUf()

            });
            
            
        }
    }
