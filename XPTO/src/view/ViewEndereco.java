    private void View_End_Bt_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_View_End_Bt_SalvarActionPerformed
        // TODO add your handling code here:
        modelEndereco = new ModelEndereco();
        modelCliente = new ModelCliente();
       
        modelEndereco.setEndereco(View_Ender_Endereco.getText());
        modelEndereco.setNumero(View_Ender_Numero.getText());
        modelEndereco.setBairro(View_Ender_Bairro.getText());
        modelEndereco.setComplemento(View_Ender_Comple.getText());
        modelEndereco.setCidade(View_Ender_Cidade.getText());
        modelEndereco.setUf(View_Ender_UF.getText());
        modelEndereco.setCep(View_Ender_CEP.getText());
        
        
        if(AltCadas.equals("Cadastrar")){
            modelEndereco.setCliente_id_cliente(Integer.parseInt(View_ende_cli_id.getText()));
            if(controllerEndereco.salvarEnderecoController(modelEndereco)){
                //cadastrou
                    JOptionPane.showMessageDialog(this, "Cadastro Realizado com Sucesso!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                    
                    String guarda = View_ende_cli_id.getText();
                    //limpar
                    limparFormulario();
                    carregarListaEndereco();
                   
                     if(!guarda.isEmpty()){

                         viewConta.enviaidcliente(modelEndereco.getCliente_id_cliente());
                         viewConta.setLocationRelativeTo(null);
                         viewConta.setVisible(true);
                         viewConta.setResizable(false);
                         setVisible(false);
                     }

                }else{
                //não cadastrou
                    JOptionPane.showMessageDialog(this, "Erro ao Cadastrar", "Erro", JOptionPane.ERROR_MESSAGE);
                }
        }else{
             int linha = View_End_Tabela.getSelectedRow();
             int idEndere = (int) View_End_Tabela.getValueAt(linha, 0);
             modelEndereco.setId_ende(idEndere);
           if(controllerEndereco.atualizarEnderecoController(modelEndereco)){
                //cadastrou
                    JOptionPane.showMessageDialog(this, "Atualização feita com Sucesso!", "Atenção", JOptionPane.INFORMATION_MESSAGE);
                    //limpar
                    limparFormulario();
                    carregarListaEndereco();
                }else{
                //não cadastrou
                    JOptionPane.showMessageDialog(this, "Erro na Atualização", "Erro", JOptionPane.ERROR_MESSAGE);
                } 
        }
        
        

    }//GEN-LAST:event_View_End_Bt_SalvarActionPerformed

