package model;

/**
 *
 * @author Advan
 */
public class ModelIntegracao {
    
   
    private String nome_clinte;
    private String nome_conta;
    private String tipo;
    private float valor;
    private String data_movi;
    private String ag;
    private String conta;

    /**
     * @return the nome_clinte
     */
    public String getNome_clinte() {
        return nome_clinte;
    }

    /**
     * @param nome_clinte the nome_clinte to set
     */
    public void setNome_clinte(String nome_clinte) {
        this.nome_clinte = nome_clinte;
    }

    /**
     * @return the nome_conta
     */
    public String getNome_conta() {
        return nome_conta;
    }

    /**
     * @param nome_conta the nome_conta to set
     */
    public void setNome_conta(String nome_conta) {
        this.nome_conta = nome_conta;
    }

    /**
     * @return the tipo
     */
    public String getTipo() {
        return tipo;
    }

    /**
     * @param tipo the tipo to set
     */
    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    /**
     * @return the valor
     */
    public float getValor() {
        return valor;
    }

    /**
     * @param valor the valor to set
     */
    public void setValor(float valor) {
        this.valor = valor;
    }

    /**
     * @return the data_movi
     */
    public String getData_movi() {
        return data_movi;
    }

    /**
     * @param data_movi the data_movi to set
     */
    public void setData_movi(String data_movi) {
        this.data_movi = data_movi;
    }

    /**
     * @return the ag
     */
    public String getAg() {
        return ag;
    }

    /**
     * @param ag the ag to set
     */
    public void setAg(String ag) {
        this.ag = ag;
    }

    /**
     * @return the conta
     */
    public String getConta() {
        return conta;
    }

    /**
     * @param conta the conta to set
     */
    public void setConta(String conta) {
        this.conta = conta;
    }

    
    
}
